
public class StringEndwith {

	public static void main(String[] args) {

		String s1="Learning java form basic";
		
		System.out.println(s1.endsWith("sic"));
		System.out.println(s1.endsWith("basic"));
		System.out.println(s1.endsWith("Basic"));
		System.out.println(s1.endsWith("c"));
		System.out.println(s1.endsWith(""));
		System.out.println(s1.endsWith("C"));




	}

}
